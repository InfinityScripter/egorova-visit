<?php
// Получение данных из формы
$name = $_POST['name'];
$email = $_POST['email'];
$message = $_POST['message'];

// Формирование заголовка письма
$to = 'e.egorova@ninevia21vek.ru'; // Замените на нужный адрес электронной почты получателя
$subject = 'Новое сообщение с формы';

// Формирование тела письма
$body = "От: $name\n";
$body .= "Email: $email\n";
$body .= "Сообщение:\n$message";

// Отправка письма
if (mail($to, $subject, $body)) {
    // Письмо успешно отправлено
    echo '<p>Спасибо! Ваше сообщение было успешно отправлено.</p>';
} else {
    // Произошла ошибка при отправке письма
    echo '<p>Извините, произошла ошибка. Пожалуйста, повторите попытку позже.</p>';
}
?>
